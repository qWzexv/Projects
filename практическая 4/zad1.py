import math

while True:
    print("Введите номер группы элементов треугольника:")
    print("1 — основание и высота")
    print("2 — две стороны и угол между ними (в радианах)")
    print("3 — три стороны")
    print("0 — выход")
    
    wer = int(input("Номер группы: "))
    
    if wer == 0:
        print("Выход из программы.")
        break
    
    if wer == 1:
        rew = float(input("Введите основание треугольника: "))
        vis = float(input("Введите высоту треугольника: "))
        to = 0.5 * rew * vis
        print("Площадь треугольника: " + str(to))
    
    elif wer == 2:
        a = float(input("Введите длину первой стороны: "))
        b = float(input("Введите длину второй стороны: "))
        age = float(input("Введите угол между сторонами (в радианах): "))
        to = 0.5 * a * b * math.sin(age)
        print("Площадь треугольника: " + str(to))
    
    elif wer == 3:
        a = float(input("Введите длину первой стороны: "))
        b = float(input("Введите длину второй стороны: "))
        c = float(input("Введите длину третьей стороны: "))
        s = (a + b + c) / 2
        to = math.sqrt(s * (s - a) * (s - b) * (s - c))
        print("Площадь треугольника: " + str(to))
    
    else:
        print("Некорректный номер группы. Пожалуйста, попробуйте снова.")
